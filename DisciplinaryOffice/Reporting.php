    <h1>Welcome to our School Disciplinary Office Reporting Center!</h1>
    <p>At STI Global City Taguig, we take the safety and well-being of our students and staff very seriously. Our dedicated School Disciplinary Office is committed to maintaining a secure and respectful learning environment for everyone. We believe that effective reporting is crucial in addressing incidents and fostering a positive school climate.</p>

    <h2>Reporting an Incident</h2>
    <p>If you have witnessed or experienced an incident that violates our school policies, we encourage you to promptly report it to our School Disciplinary Office. We have established a simple and confidential reporting system to ensure that your concerns are addressed with the utmost care and attention.</p>

    <p>To report an incident, please click on the "Report an Incident" button below. You will be directed to a secure online form where you can provide detailed information about the event. We kindly request that you provide as much information as possible, including the date, time, location, and any witnesses involved. Your identity will remain strictly confidential unless you choose to disclose it.</p>

    <a href="/DO/stylesheet/Login.php" class="report-button">Report an Incident</a>

    <h2>Our commitment to confidentiality</h2>
    <p>We understand that reporting an incident may be a sensitive matter. Rest assured that all reports submitted to the School Disciplinary Office will be handled with the highest level of confidentiality. We are dedicated to protecting the privacy of those involved while conducting a thorough investigation to address the issue appropriately.</p>

    <h2>Investigation and follow-up</h2>
    <p>Once an incident is reported, our School Disciplinary Office will initiate an investigation promptly. Our team of experienced professionals will review the information provided and take necessary actions to address the situation. We may reach out to witnesses, gather additional evidence, and collaborate with other relevant school personnel to ensure a fair and comprehensive investigation.</p>

    <p>After the investigation, appropriate disciplinary measures will be taken based on our school policies and the severity of the incident. We will also prioritize providing support and resources to those affected to promote healing and prevent future occurrences.</p>

    <p>Please note that filing a false report is a serious offense and may result in disciplinary actions. We encourage you to use this reporting system responsibly and truthfully to maintain the integrity of our school community.</p>

    <h2>Your voice matters</h2>
    <p>By reporting incidents, you play a vital role in upholding our school values and ensuring a safe environment for everyone. We appreciate your willingness to stand up against misconduct and contribute to a positive and inclusive school community. Together, we can create an environment where all students can thrive academically, emotionally, and socially.</p>

    <p>If you have any questions or need further assistance, please do not hesitate to reach out to our School Disciplinary Office. We are here to support you and address any concerns you may have.</p>
