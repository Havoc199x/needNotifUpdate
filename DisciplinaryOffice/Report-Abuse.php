    <h1>Report Abuse</h1>
    <p>The Disciplinary Office in STI Global City Taguig is committed to providing a safe and respectful environment for everyone. We do not tolerate any form of abusive behavior or activity, and we take all reports seriously. Please review the following
        types of abuse and their descriptions, along with the penalties or laws in the Philippines:</p>

    <h2>Bullying</h2>
    <p>Bullying is any repeated, intentional behavior that is meant to hurt, harm, or humiliate another person, either physically, verbally, or emotionally. This includes cyberbullying, which involves the use of electronic communication to bully or harass
        someone. The Anti-Bullying Act of 2013 (Republic Act No. 10627) prohibits bullying in schools and other educational institutions and imposes penalties for violations.</p>

    <h2>Harassment</h2>
    <p>Harassment is any unwanted or unwelcome behavior that is meant to intimidate, offend, or threaten another person, either physically, verbally, or sexually. This includes sexual harassment, which involves any unwelcome sexual advances, requests for
        sexual favors, or other verbal or physical conduct of a sexual nature. The Anti-Sexual Harassment Act of 1995 (Republic Act No. 7877) prohibits sexual harassment in the workplace and imposes penalties for violations.</p>

    <h2>Discrimination</h2>
    <p>Discrimination is any unfair or unequal treatment of a person based on their race, ethnicity, gender, age, disability, religion, or other personal characteristics. This includes any exclusion, restriction, or preference that has the effect of nullifying
        or impairing the recognition, enjoyment, or exercise of a person's rights and freedoms. The Philippine Constitution and various laws, such as the Magna Carta for Persons with Disabilities (Republic Act No. 7277) and the Anti-Age Discrimination
        in Employment Act (Republic Act No. 10911), prohibit discrimination and impose penalties for violations.</p>

    <h2>Threats or Violence</h2>
    <p>Threats or violence are any statements or actions that are meant to instill fear or cause harm to another person, either physically or mentally. This includes any physical assault, battery, or intimidation, as well as any verbal or written threats.
        The Revised Penal Code of the Philippines imposes penalties for crimes such as physical injuries, grave threats, and coercion.</p>
